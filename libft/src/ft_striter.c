#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int i;
	int j;

	if (!s || !f)
		return ;
	j = 0;
	i = ft_strlen(s);
	while (j < i)
	{
		f(&s[j]);
		j++;
	}
}
